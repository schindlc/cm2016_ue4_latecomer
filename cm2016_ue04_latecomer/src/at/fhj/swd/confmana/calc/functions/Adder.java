package at.fhj.swd.confmana.calc.functions;

/**
 * adder class to calculate sum of two passed doubles
 */
public class Adder {
	
	/**
	 * adds the passed doubles and return the sum
	 * @param first first addend
	 * @param second second addend
	 * @return sum
	 */
	public double add(double first, double second){
		return first+second;
	}

}
